class Referencia {
    String objeto;       
    int paginaVirtual;   
    int desplazamiento;  
    String accion;       

    public Referencia(String objeto, int paginaVirtual, int desplazamiento, String accion) {
        this.objeto = objeto;
        this.paginaVirtual = paginaVirtual;
        this.desplazamiento = desplazamiento;
        this.accion = accion;
    }
}

